    .text

#=====================================================
#   Boot handler
#=====================================================
boot_handler:

    #---------------------------------------------
    #   Set $sp=0x8000_0000
    #---------------------------------------------
    LUI     $sp, 0x8000

    #---------------------------------------------
    #   Jump to user code
    #---------------------------------------------
    LUI     $k0, 0x0000
    ORI     $k0, 0x8000
    JALR    $k0


#=====================================================
#   EXIT
#=====================================================

exit:

    NOP
    NOP
    NOP
    NOP
    NOP
    LUI $t0, 0xF000
    JR  $t0
